def main():
    print("Вітаю, користувачу!")
    print("Оберіть персонажа:")
    characters = ["Райден", "Невлета", "Навія", "Фуріна"]
    for i, char in enumerate(characters, 1):
        print(f"{i}. {char}")

    choice = input("Введіть номер або ім'я персонажа: ").strip().lower()
    if choice.isdigit() and 1 <= int(choice) <= len(characters):
        display_character_info(characters[int(choice) - 1])
    elif choice.capitalize() in characters:
        display_character_info(choice.capitalize())
    else:
        print("Невірний вибір. Будь ласка, спробуйте ще раз.")


if __name__ == "__main__":
    main()